package com.example.fethi.sinavzauygulama.veli.veliAdapters;

public class VeliListItemOzetHorizontal {
    private int img;
    private String ust;
    private String alt;

    public VeliListItemOzetHorizontal(int img, String ust, String alt) {
        this.img = img;
        this.ust = ust;
        this.alt = alt;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getUst() {
        return ust;
    }

    public void setUst(String ust) {
        this.ust = ust;
    }

    public String getAlt() {
        return alt;
    }

    public void setAlt(String alt) {
        this.alt = alt;
    }
}
